# 5-3 Alien's Color


# Alien Color: green, yellow, red
alien_color = 'green'
print("Killed an " + alien_color + " alien....\n")

# Version 1: If alien color is green earn 5 points
print("Version 1")
if alien_color == 'green':
    print("Earned 5 points.")
print()

# Version 2: If alien color is green earn 5 points, else earn 10 points
print("Version 2")
if alien_color == 'green':
    print("Earned 5 points.")
else:
    print("Earned 10 points.")
print()


# Version 3:
# If alien color is green earn 5 points,
# elif color is yellow earn 10 points,
# elif color is red earn 15 points
print("Version 3")
if alien_color == 'green':
    print("Earned 5 points.")
elif alien_color == 'yellow':
    print("Earned 10 points.")
elif alien_color == 'red':
    print("Earned 15 points.")
print()






